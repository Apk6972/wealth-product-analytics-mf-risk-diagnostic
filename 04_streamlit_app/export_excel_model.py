def _build_memo_inputs(
    fund_master: pd.DataFrame,
    metrics_summary: pd.DataFrame,
    stress_results: pd.DataFrame,
    attribution_results: pd.DataFrame,
    suitability_results: pd.DataFrame,
) -> pd.DataFrame:
    """
    Build a one-row-per-fund "talking points" sheet for writing the Notion
    methodology note / LinkedIn carousel without re-deriving figures by
    hand. Every column here is a direct re-selection, groupby, or pivot of
    an existing processed-data column — nothing is recomputed or estimated.
    """
    empty_columns = [
        "fund_label", "category", "sleeve", "primary_benchmark", "cagr", "annualized_volatility",
        "sharpe_ratio", "max_drawdown", "daily_cvar_95", "recovery_period_days",
        "worst_stress_scenario", "worst_stress_loss", "largest_loss_contributor_scenario_count",
        "role_conservative", "role_balanced", "role_growth", "role_aggressive",
    ]
    if not is_dataframe_usable(metrics_summary):
        return pd.DataFrame(columns=empty_columns)

    base_columns = [
        "fund_label", "cagr", "annualized_volatility", "sharpe_ratio", "max_drawdown", "daily_cvar_95",
        "recovery_period_days",
    ]
    memo_df = metrics_summary[base_columns].copy()

    if is_dataframe_usable(fund_master):
        fund_lookup = fund_master[["fund_label", "category", "sleeve", "primary_benchmark"]].drop_duplicates(
            "fund_label"
        )
        memo_df = memo_df.merge(fund_lookup, on="fund_label", how="left")

    if is_dataframe_usable(stress_results):
        worst_per_fund = (
            stress_results.sort_values("stress_return")
            .groupby("fund_label", as_index=False)
            .first()[["fund_label", "scenario_name", "stress_return"]]
            .rename(columns={"scenario_name": "worst_stress_scenario", "stress_return": "worst_stress_loss"})
        )
        memo_df = memo_df.merge(worst_per_fund, on="fund_label", how="left")

    if is_dataframe_usable(attribution_results) and "is_largest_loss_contributor" in attribution_results.columns:
        is_largest = attribution_results["is_largest_loss_contributor"].fillna(False).astype(bool)
        contributor_counts = (
            attribution_results.loc[is_largest]
            .groupby("fund_label")
            .size()
            .rename("largest_loss_contributor_scenario_count")
            .reset_index()
        )
        memo_df = memo_df.merge(contributor_counts, on="fund_label", how="left")
        memo_df["largest_loss_contributor_scenario_count"] = (
            memo_df["largest_loss_contributor_scenario_count"].fillna(0).astype(int)
        )

    if is_dataframe_usable(suitability_results):
        role_pivot = suitability_results.pivot_table(
            index="fund_label", columns="client_profile", values="suitability_role", aggfunc="first"
        )
        role_pivot.columns = [f"role_{str(profile).lower()}" for profile in role_pivot.columns]
        role_pivot = role_pivot.reindex(columns=[f"role_{profile.lower()}" for profile in CLIENT_PROFILES])
        memo_df = memo_df.merge(role_pivot.reset_index(), on="fund_label", how="left")

    return memo_df


