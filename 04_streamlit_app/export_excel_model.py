def _style_header_row(ws: Worksheet, header_row: int, n_columns: int) -> None:
    for col_idx in range(1, n_columns + 1):
        cell = ws.cell(row=header_row, column=col_idx)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)


def _autosize_columns(ws: Worksheet, df: pd.DataFrame, start_col: int = 1) -> None:
    for offset, column in enumerate(df.columns):
        col_idx = start_col + offset
        try:
            content_width = int(df[column].astype(str).map(len).max()) if len(df) else 0
        except Exception:  # noqa: BLE001 - width estimation must never break the export
            content_width = 0
        header_width = len(str(column))
        width = max(MIN_COLUMN_WIDTH, min(MAX_COLUMN_WIDTH, max(content_width, header_width) + 2))
        ws.column_dimensions[get_column_letter(col_idx)].width = width


def _apply_date_format(ws: Worksheet, df: pd.DataFrame, header_row: int, date_columns: List[str]) -> None:
    n_rows = len(df)
    if n_rows == 0:
        return
    for column in date_columns:
        if column not in df.columns:
            continue
        col_idx = list(df.columns).index(column) + 1
        for row in ws.iter_rows(min_row=header_row + 1, max_row=header_row + n_rows, min_col=col_idx, max_col=col_idx):
            row[0].number_format = "yyyy-mm-dd"


def _write_meta_block(ws: Worksheet, title: str, source_note: str, extra_notes: Optional[List[str]] = None) -> int:
    """Write the title / source-note / extra-notes / last-refreshed block starting at row 1.
    Returns the 1-indexed row number of the blank separator row immediately below the block
    (i.e. the row the data table's header should occupy is this value + 1)."""
    row = 1
    ws.cell(row=row, column=1, value=title).font = TITLE_FONT
    row += 1
    ws.cell(row=row, column=1, value=f"Data source: {source_note}").font = SUBTITLE_FONT
    row += 1
    for note in extra_notes or []:
        ws.cell(row=row, column=1, value=note).font = SUBTITLE_FONT
        row += 1
    ws.cell(row=row, column=1, value=LAST_REFRESHED_NOTE).font = SUBTITLE_FONT
    row += 1
    return row


def _write_data_table(
    ws: Worksheet, df: pd.DataFrame, header_row: int, date_columns: Optional[List[str]] = None
) -> int:
    """Write a DataFrame's header + rows directly via openpyxl cell writes (used for the two
    hand-composed sheets: 00_Model_Control and 10_Benchmark_Relative). Returns the next free row."""
    has_data = is_dataframe_usable(df)
    df_to_write = df if has_data else pd.DataFrame(columns=list(df.columns) if df is not None else [])

    for col_idx, column in enumerate(df_to_write.columns, start=1):
        ws.cell(row=header_row, column=col_idx, value=str(column))
    for row_offset, record in enumerate(df_to_write.itertuples(index=False, name=None), start=1):
        for col_idx, value in enumerate(record, start=1):
            ws.cell(row=header_row + row_offset, column=col_idx, value=value)

    if len(df_to_write.columns) > 0:
        _style_header_row(ws, header_row=header_row, n_columns=len(df_to_write.columns))
        _autosize_columns(ws, df_to_write)
        if date_columns:
            _apply_date_format(ws, df_to_write, header_row=header_row, date_columns=date_columns)

    if not has_data:
        ws.cell(row=header_row + 1, column=1, value=NO_DATA_NOTE).font = MISSING_FONT
        return header_row + 2
    return header_row + len(df_to_write) + 1


def _write_table_sheet(
    writer: "pd.ExcelWriter",
    sheet_name: str,
    title: str,
    source_note: str,
    df: pd.DataFrame,
    extra_notes: Optional[List[str]] = None,
    date_columns: Optional[List[str]] = None,
) -> Worksheet:
    """
    Write one processed dataset to its own sheet with a title / data-source /
    last-refreshed meta block above a styled, auto-sized data table (bulk
    written via pandas.to_excel for speed on the larger datasets). Gracefully
    degrades (still produces a valid, clearly-labelled sheet) if the source
    dataset is missing or empty.
    """
    has_data = is_dataframe_usable(df)
    df_to_write = df if has_data else pd.DataFrame(columns=list(df.columns) if df is not None else [])

    meta_line_count = 3 + len(extra_notes or [])  # title + source note + extra notes + last-refreshed
    header_row_0idx = meta_line_count  # to_excel startrow is 0-indexed; this leaves exactly one blank row above
    df_to_write.to_excel(writer, sheet_name=sheet_name, index=False, startrow=header_row_0idx)
    ws = writer.sheets[sheet_name]

    _write_meta_block(ws, title=title, source_note=source_note, extra_notes=extra_notes)
    header_row = header_row_0idx + 1
    if not has_data:
        ws.cell(row=header_row + 1, column=1, value=NO_DATA_NOTE).font = MISSING_FONT

    if len(df_to_write.columns) > 0:
        _style_header_row(ws, header_row=header_row, n_columns=len(df_to_write.columns))
        _autosize_columns(ws, df_to_write)
        if date_columns:
            _apply_date_format(ws, df_to_write, header_row=header_row, date_columns=date_columns)
        ws.freeze_panes = ws.cell(row=header_row + 1, column=1).coordinate
    return ws


def _append_table_block(
    ws: Worksheet, start_row: int, section_title: str, df: pd.DataFrame, date_columns: Optional[List[str]] = None
) -> int:
    """Append a secondary titled table further down an already-open sheet (used by
    10_Benchmark_Relative to show static full-history metrics and rolling 252D metrics on one
    sheet). Returns the next free row after the appended block plus one blank padding row."""
    ws.cell(row=start_row, column=1, value=section_title).font = SECTION_FONT
    header_row = start_row + 1
    next_row = _write_data_table(ws, df, header_row=header_row, date_columns=date_columns)
    return next_row + 1


